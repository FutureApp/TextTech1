package texttechno.task1.arunner;

public class TupelSI {
	String key;
	Integer Value;

	public TupelSI(String key, Integer value) {
		super();
		this.key = key;
		Value = value;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public Integer getValue() {
		return Value;
	}

	public void setValue(Integer value) {
		Value = value;
	}

}
